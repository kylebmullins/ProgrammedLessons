public class Rain {
    public static void main(String[] args) {
        double a = 12;
        double m = 14;
        double j = 8;
        double av = (a + m + j) / 3;
        System.out.println("Rainfall for April: " + a);
        System.out.println("Rainfall for May: " + m);
        System.out.println("Rainfall for June: " + j);
        System.out.println("Average rainfall: " + av);
    }
}